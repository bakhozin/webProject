package com.sist.vo;

public class GiftyconAccount {
	private int G_dealNum;
	private String giftyconCode;
	private String regdate;
	private String mid;
	private String recipient;
	private String expireDate;
	
	public int getG_dealNum() {
		return G_dealNum;
	}
	public void setG_dealNum(int s_dealNum) {
		G_dealNum = s_dealNum;
	}
	public String getGiftyconCode() {
		return giftyconCode;
	}
	public void setGiftyconCode(String giftyconCode) {
		this.giftyconCode = giftyconCode;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getRecipient() {
		return recipient;
	}
	public void setRecipient(String recipient) {
		this.recipient = recipient;
	}
	public String getExpireDate() {
		return expireDate;
	}
	public void setExpireDate(String expireDate) {
		this.expireDate = expireDate;
	}
	
	
}
