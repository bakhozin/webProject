package com.sist.vo;

public class Giftycon {
	
	private String giftyconCode;
	private String giftyconName;
	private String content;
	private int price;
	private String image;
	

	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getGiftyconCode() {
		return giftyconCode;
	}
	public void setGiftyconCode(String giftyconCode) {
		this.giftyconCode = giftyconCode;
	}
	public String getGiftyconName() {
		return giftyconName;
	}
	public void setGiftyconName(String giftyconName) {
		this.giftyconName = giftyconName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
	
}
