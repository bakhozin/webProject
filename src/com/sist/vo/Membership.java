package com.sist.vo;

public class Membership {
	private String mid;
	private int acc_point;
	private int usable_point;
	private String grade;
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public int getAcc_point() {
		return acc_point;
	}
	public void setAcc_point(int acc_point) {
		this.acc_point = acc_point;
	}
	public int getUsable_point() {
		return usable_point;
	}
	public void setUsable_point(int usable_point) {
		this.usable_point = usable_point;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	
	
}
